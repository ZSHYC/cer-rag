def normalize_text(text: str) -> str:
    """修复文本中的 pdftotext 乱码"""
    for garbled, correct in SYMBOL_FIXES.items():
        text = text.replace(garbled, correct)

    # 合并重复标点（pdftotext 常见 artifact）
    text = re.sub(r'\.{3,}', '...', text)
    text = re.sub(r' {2,}', ' ', text)

    # 清除孤立的 Unicode 组合字符
    text = re.sub(r'[̀-ͯ᪰-᫿⃐-⃿︀-️]+', '', text)

    # 规范换行
    text = re.sub(r'\n{3,}', '\n\n', text)

    return text.strip()


def normalize_file(input_path: str, output_path: str) -> str:
    """修复文件中的乱码，写入新文件，返回修复后文本"""
    with open(input_path, 'r', encoding='utf-8') as f:
        raw = f.read()

    cleaned = normalize_text(raw)

    with open(output_path, 'w', encoding='utf-8') as f:
        f.write(cleaned)

    return cleaned
